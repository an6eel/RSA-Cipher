    private void encryptbutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_encryptbutActionPerformed
        System.out.println(tocrypt.getText());
        if(tocrypt.getText().isEmpty())
            msgtocrypt.setVisible(true);
        else{
            msgtocrypt.setVisible(false);
            encrylabel.setVisible(true);
            messageencrypted.setVisible(true);
            messageencrypted.setText(cipher.encrypt(tocrypt.getText()).toString());
            tocrypt.setText("");
            clipboard.setEnabled(true);
        }
        
    }//GEN-LAST:event_encryptbutActionPerformed

