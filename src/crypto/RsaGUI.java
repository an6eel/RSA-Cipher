    private void keysbutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_keysbutActionPerformed
        File publ=new File("public.key");
        File priv=new File("private.key");
        FileWriter pwriter,privwriter;
        if(!publ.exists()){
            try {
                pwriter = new FileWriter(publ,true);
                privwriter=new FileWriter(priv,true);
                pwriter.write("Public Exponent: "+ cipher.getPublicKey().getExponent().toString()+ "\n"+ "Modulus: "+cipher.getPublicKey().getModulus().toString());
                privwriter.write("Private Exponent: "+ cipher.getPrivateKey().getExponent().toString()+ "\n"+ "Modulus: "+cipher.getPrivateKey().getModulus().toString());
                pwriter.close();
                privwriter.close();

            }  catch (IOException ex) {
                Logger.getLogger(RsaGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_keysbutActionPerformed

